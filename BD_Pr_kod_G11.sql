-------------------------------------
--   CREATE PERSONAL DATA
-------------------------------------
CREATE TABLE PERSONAL_DATA(
    PERSONAL_DATA_ID NUMBER,
    FIRST_NAME VARCHAR2(30 CHAR) NOT NULL,
    LAST_NAME VARCHAR2(30 CHAR) NOT NULL,
    ADDRESS_ID NUMBER NOT NULL,
    PHONE_NUMBER VARCHAR2(9 CHAR),
