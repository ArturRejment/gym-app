CREATE OR REPLACE VIEW AVAILABLE_TRAINERS_VIEW AS
SELECT PERSONAL_DATA.LAST_NAME AS "Last name", PERSONAL_DATA.FIRST_NAME AS "First name",
WORKING_HOURS.START_TIME AS "Start", WORKING_HOURS.FINISH_TIME AS "Finish"
FROM TRAINER_HOURS
JOIN WORKING_HOURS USING(WORKING_ID)
JOIN TRAINER USING(TRAINER_ID)
JOIN PERSONAL_DATA ON TRAINER.PERSONAL_DATA_ID=personal_data.personal_data_id
WHERE IS_ACTIVE = 1 AND MEMBER_ID IS NULL
ORDER BY PERSONAL_DATA.LAST_NAME DESC;

CREATE OR REPLACE VIEW ACTIVE_MEMBERSHIPS_VIEW AS
SELECT FIRST_NAME AS "First name", LAST_NAME AS "Last name",
EXPIRY_DATE AS "Expiry date", MEMBERSHIP_TYPE AS "Membership type"
FROM member_memberships
JOIN GYM_MEMBER USING(MEMBER_ID)
JOIN PERSONAL_DATA USING(PERSONAL_DATA_ID)
JOIN MEMBERSHIP USING(MEMBERSHIP_ID)
WHERE member_memberships.expiry_date > CURRENT_DATE;

CREATE OR REPLACE VIEW PRODUCTS_IN_SHOPS_VIEW AS
SELECT SHOP_NAME AS "Shop name", PRODUCT_NAME as "Product name",
PRODUCT_PRICE as "Price [zl]", PRODUCT_AMOUNT as "Amount"
FROM SHOP_PRODUCTS
JOIN PRODUCTS USING(PRODUCT_ID)
JOIN SHOP USING(SHOP_ID)
ORDER BY SHOP_NAME ASC;

CREATE OR REPLACE VIEW GROUP_TRAININGS_LIST_VIEW AS
SELECT GROUP_TRAINING_NAME AS "Training name", FIRST_NAME AS "First name",
LAST_NAME AS "Last name", START_TIME AS "Start", FINISH_TIME AS "Finish"
FROM GROUP_TRAINING
JOIN WORKING_HOURS USING(WORKING_ID)
JOIN TRAINER USING(TRAINER_ID)
JOIN PERSONAL_DATA USING(PERSONAL_DATA_ID);

CREATE OR REPLACE VIEW MEMBERSHIPS_LIST_VIEW AS
SELECT MEMBERSHIP_TYPE AS "Type", MEMBERSHIP_PRICE AS "Price"
FROM MEMBERSHIP;

