CREATE OR REPLACE TRIGGER "UPDATE_MEMBERS_HISTORY_LOG"
AFTER UPDATE OF is_suspended ON GYM_MEMBER
FOR EACH ROW
BEGIN
        add_member_history(:old.member_id, :old.sign_up_date, sysdate, :old.is_suspended);
END;
